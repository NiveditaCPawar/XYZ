package For_Loop;

public class Loops_while {

	public static void main(String[] args) {
		int i=5;//initialization
		do{
			System.out.println(i);
			i=i+5;//updation
		}
		while(i<=50);//condition
		
		System.out.println("==============");
		
		
		
		int a=40;//initialization
		do {
			System.out.println(a);
			a=a-4;//updation
		}while(a>=4);//condition
		
		System.out.println("==============");
		
		
		

	}

}
